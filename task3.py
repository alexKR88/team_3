# Ввод начальных значений
a = 0
b = 1000
print("Загадайте число от 0 до 1000. Отвечайте 'да' или 'нет' на вопросы.")
while a <= b:
    num = (a + b) // 2
    res = input(f"Ваше число больше {num}? ").strip().lower()
    # Проверка корректности ответа
    while res not in ('да', 'нет'):
        print("Некорректный ответ. Пожалуйста, введите 'да' или 'нет'.")
        res = input(f"Ваше число больше {num}? ").strip().lower()
    if res == 'да':
        a = num + 1
    else:
        b = num - 1
# Вывод результата
print(f"\nЯ угадал! Ваше число: {a}")